public class RelationTester {
    public static void main(String[] args) {
        int x = 19;
        int y = 17;
        int z = 4;
        System.out.println("x="+x);
        System.out.println("y="+y);
        System.out.println("z="+z);

        System.out.println("x>y吗？"+(x>y));
        System.out.println("x<y吗？"+(x<y));
        System.out.println("x==y吗？"+(x==y));
        System.out.println("x!=y吗？"+(x!=y));

        System.out.println("x>=z吗？"+(x>=z));
        System.out.println("x<=z吗？"+(x<=z));
        System.out.println("x==z吗？"+(x==z));

        System.out.println("\n布尔存储变量显示：");
        boolean isGreater = (x>y);
        boolean isequal = (x==y);
        System.out.println("x>y吗？"+isGreater);
        System.out.println("x<y吗？"+isequal);





    }
}
